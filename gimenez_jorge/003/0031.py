class Persona(self, nombre, apellido):
	def set_nombre(self):
		nombre = input("Ingrese su nombre: ")

	def set_apellido(self):
		apellido = input("Ingrese su apellido: ")

	def get_nombre(self):
		nombre = self.nombre
		return nombre

	def get_apellido(self):
		apellido = self.apellido
		return apellido